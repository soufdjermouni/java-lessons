package com.interview.errorcompile;

public class Exemple1 {

    public static void main(String[] args) {
        int num = 100;

        //Since if block doesn't contain any block it will remain the same. And those {} brackets will be like instance
        // initialize block so output will be Lazy Programmer.

        if(num==101);{
            System.out.println("Lazy Programmer");
        }
    }
}
